		                        <p>Xabi Alonso analizó en rueda de prensa la actualidad del Real Madrid. El jugador
		                        de Tolosa reconoció que tiene muy buena relación con Cristiano Ronaldo, destacó 
		                        la calidad de Ribéry, analizó las opciones de España en el Mundial de Sudáfrica 
		                        del próximo verano, los dos puntos que le separan del Barça en Liga y la vuelta 
		                        de octavos de Liga de Campeones que jugarán en el Bernabéu ante el Olympique de Lyon.</p>

								<p>Mucho se habló del penalti cometido sobre Cristiano Ronaldo ante el Villarreal y 
								que finalmente se encargó de lanzar y transformar Xabi Alonso. El portugués no 
								celebró el gol  con su compañero y la polémica estaba servida, a pesar de que el luso
								comentó que no estaba enfadado.</p>

								<cite>"Ribéry es muy buen jugador, no lo voy a descubrir yo"</cite>

								<p>Este jueves le han preguntado a Xabi Alonso al respecto y el ex jugador del Liverpool
								ha sido tajante: "Cada uno puede comentar lo que quiera pero mi relación con CR9 es
								fantástica. El otro día tiró CR9 un penati, el anterior lo tiré yo, pero igual otro
								día no lo tiro yo", comentó.</p>




								<p>En cuanto a la polémica generada y a las versiones que se dieron al respecto, 
								Xabi prefirió mantenerse al margen. "No quiero entrar en ese tema. Ví una 
								versión que era la buena y otra que no", sentenció.</p>
